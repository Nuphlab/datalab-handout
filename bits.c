int isNotEqual(int x, int y) {
    return !(!((~x+1)+y));
}
/* 
 * negate - return -x 
 *   Example: negate(1) = -1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 5
 *   Rating: 2
 */
int negate(int x) {
    // flips the bits and adds one to give you the opposite signed number
    return (~x) + 1;
}
