int isPositive(int x) {

  return !(x >> 31) & !!x;
}
/* 
 * isNonNegative - return 1 if x >= 0, return 0 otherwise 
 *   Example: isNonNegative(-1) = 0.  isNonNegative(0) = 1.
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int isNonNegative(int x) {
    // if the msb is 1 then we'll return a zero, and vice versa
    // This checks the sign bit to prove our return statement.
    return ((x >> 31) + 1);
}
/* 
 * rotateLeft - Rotate x to the left by n
 *   Can assume that 0 <= n <= 31
 *   Examples: rotateLeft(0x87654321,4) = 0x76543218
 *   Legal ops: ~ & ^ | + << >> !
 *   Max ops: 25
 *   Rating: 3 
 */

int rotateLeft(int x, int n) {
    // Find 32 - n  and creat mask
    // a is the bits being shifted
    // Make x the rotated bytes and no shifted bits added
    int a, b, c;
    c = 32 + ~n;
    b = ~((~0x0) << n);
    a = (x >> c) >> 1;
    a = b & a;
    x = x << n;
    return x|a;
}
/* 
 * addOK - Determine if can compute x+y without overflow
 *   Example: addOK(0x80000000,0x80000000) = 0,
 *            addOK(0x80000000,0x70000000) = 1, 
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 20
 *   Rating: 3
 */
int addOK(int x, int y) {
    /* check the signs of the variables
     * if signs are diff, then no overflow
     * if signs are same, check that x
     * and y are different than the combined
     * if not diff, then no overflow
    */
    int combined = x + y;
  return !(((combined ^ x) & (combined ^ y)) >> 31);
}
