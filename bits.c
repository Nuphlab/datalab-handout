/* 
 * bitAnd - x&y using only ~ and | 
 *   Example: bitAnd(6, 5) = 4
 *   Legal ops: ~ |
 *   Max ops: 8
 *   Rating: 1
 */
int bitAnd(int x, int y) {
    // The and operator will compare each binary digit across two integers
    // Returns 1 if both are 1 for each bit
    // We'll negate each, then negate the result to account for pos/neg
  return ~(~x|~y);
}
/* 
 * bitOr - x|y using only ~ and & 
 *   Example: bitOr(6, 5) = 7
 *   Legal ops: ~ &
 *   Max ops: 8
 *   Rating: 1
 */
int bitOr(int x, int y) {
    // The or operator will compare each binary digit across two integers
    // Returns 1 if any are 1 for each bit
    // We'll negate each, then negate the result to account for pos/neg
  return ~(~x&~y);
}
